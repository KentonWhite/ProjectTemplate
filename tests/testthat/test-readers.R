test_that("Test 1: CSV Data file", {
    test_project <- tempfile("test_project")
    suppressMessages(create.project(basename(test_project), project.directory = dirname(test_project)))
    on.exit(unlink(test_project, recursive = TRUE), add = TRUE)

    oldwd <- setwd(test_project)
    on.exit(setwd(oldwd), add = TRUE)
    suppressMessages(load.project())

    data.file <- "example_01.csv"
    filename <- file.path(
        system.file("example_data",
            package = "ProjectTemplate"
        ),
        "example_01.csv"
    )
    variable.name <- clean.variable.name("example_01")

    csv.reader(data.file, filename, variable.name)

    expect_true(exists(variable.name))
    expect_equal(names(get(variable.name)), c("N", "Prime"))
    expect_equal(nrow(get(variable.name)), 5)
    expect_equal(ncol(get(variable.name)), 2)
    expect_equal(get(variable.name)[5, 2], 11)
    rm(example_01, envir = .TargetEnv)
})


